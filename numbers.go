// Package number provides constants for known MySQL (5.6) error numbers and some simple utilities
package number

type Number uint16

func (n Number) GlobalError() bool {
	return n >= 1 && n < 1000
}

func (n Number) ServerError() bool {
	return n >= 1000 && n < 2000
}

func (n Number) ClientError() bool {
	return n >= 2000 && n < 3000
}

